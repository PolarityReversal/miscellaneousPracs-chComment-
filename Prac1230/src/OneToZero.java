public class OneToZero {
	public static void main(String[] Args) {
		for (int i = 1; i <= 60; i += 10) {
			System.out.print("         |");
		}
			System.out.println();
		for (int j = 1; j <= 6; j++) {
			for (int k = 1; k <= 9; k++) {
				System.out.print(k);
		}
				System.out.print("0");
	}
}
}